import pytest
from f081 import numerical_letter_grade

def test_numerical_letter_grade():
    grades = [4.0, 3.8, 3.4, 3.1, 2.8, 2.4, 2.1, 1.8, 1.4, 1.1, 0.8, 0.5, 0.0]
    expected = ["A+", "A", "A-", "B+", "B", "B-", "C+", "C", "C-", "D+", "D", "D-", "E"]
    result = numerical_letter_grade(grades)
    assert result == expected

    # Test edge cases
    edge_grades = [3.7, 3.3, 3.0, 2.7, 2.3, 2.0, 1.7, 1.3, 1.0, 0.7, 0.0]
    edge_expected = ["A-", "B+", "B", "B-", "C+", "C", "C-", "D+", "D", "D-", "E"]
    edge_result = numerical_letter_grade(edge_grades)
    assert edge_result == edge_expected
